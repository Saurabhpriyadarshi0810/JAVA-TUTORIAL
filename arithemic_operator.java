public class arithemic_operator {
    public static void main(String[] args) {
        int p = 15 , q = 5 ;

        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
        System.out.println(p%q);

        System.err.println(++p);
        System.err.println(--p);
        System.err.println(p--);
        System.err.println(p++);
    }
    
}
