import java.util.Scanner;
public class ArithmeticOperator {
    public static void main (String[] args){
        int a = 7;
        int b = 8;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
