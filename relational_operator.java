public class relational_operator {
    public static void main(String[] args) {
        int a = 15, b = 5;

        System.err.println(a == b);
        System.err.println(a != b);
        System.err.println(a > b);
        System.err.println(a < b);
        System.err.println(a >= b);
        System.err.println(a <= b);
    }

}
